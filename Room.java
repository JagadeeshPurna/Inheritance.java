package Home_Work;

public class Room {
	private Tv tv;
	private Fan fan;
	private Chairs chairs;
	
	
	public Tv getTv() {
		return tv;
	}
	public void setTv(Tv tv) {
		this.tv = tv;
	}
	public Fan getFan() {
		return fan;
	}
	public void setFan(Fan fan) {
		this.fan = fan;
	}
	public Chairs getChairs() {
		return chairs;
	}
	public void setChairs(Chairs chairs) {
		this.chairs = chairs;
	}

	

}
